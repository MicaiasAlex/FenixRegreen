        <h2>O que é Neurodiversidade?</h2>
        <p>Reconhecendo a Pluralidade do Cérebro Humano
<b>É um conceito fundamental que reconhece a diversidade natural das funções cerebrais e comportamentais humanas.</b> 
Em essência, ela propõe que as variações neurológicas, como o Transtorno do Espectro Autista (TEA), TDAH (Transtorno do Déficit de Atenção com Hiperatividade), dislexia e dispraxia, não devem ser vistas como 
doenças ou transtornos a serem "curados", mas sim como diferenças normais e valiosas na espécie humana.</p>
        <p><b>Entendendo os Termos Chave</b></br>
A compreensão da neurodiversidade se baseia em alguns termos essenciais:<br>
Neurodiversidade: O conceito amplo de que existe uma grande variedade de composições 
e funcionamentos neurológicos entre as pessoas, e que essa variação é natural e deve ser respeitada.<br><br>

Neurotípico: Refere-se a um indivíduo cujo funcionamento neurológico se enquadra no que é considerado o padrão esperado pela sociedade.<br><br>

Neurodivergente: É o indivíduo que apresenta um funcionamento neurológico atípico, fora do padrão esperado pela sociedade. Essas variações cognitivas incluem:<br>
<li>
    <lu>
        <li>Transtorno do Espectro Autista (TEA)</li>
        <li>TDAH</li>
        <li>Dislexia (dificuldade de leitura e escrita)</li>
        <li>Dispraxia (disfunção que compromete a capacidade de coordenação)</li>
        <li>Discalculia (dificuldade com matemática), entre outras.</li>
    </lu>
</li>
</p>
